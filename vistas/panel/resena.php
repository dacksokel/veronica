        El 16 de Septiembre de 1936, el Consejo Municipal del Distrito Bermúdez decreta la construcción de una Escuela Federal Graduada que llevaría el nombre del célebre cumanés “Pedro Elías Aristeguieta” iniciando sus actividades educativas en la calle Independencia frente a la Plaza Bolívar, esta escuela fue dirigida en sus primeros años por Manuel Mujica, acompañado por los educadores: Juanita Salinas y Salvador Galloni, con una matrícula de 120 estudiantes.<br><br>

Para el año de 1952, es trasladada al final de la calle Bolívar con sede propia s/n al lado de la Guarnición Militar ( Infantería de Marina), en donde cumplían labores un director Lorenzo Castillo Brito, 1 subdirector, 1 secretaria y 12 maestros de aula, dejando de ser Escuela Federal para convertirse en Grupo Escolar “Pedro Elías Aristeguieta”.<br><br>

En 1958, el plantel contaba con 20 maestros de aula, 1 secretaria y un 1 maestro especialista, siendo para esa fecha un plantel de primera categoría.<br><br>

Para 1969, luego de haber cesado en sus funciones el director anterior es  nombrado para dirigir Ángel Ramón Luna, en ese momento la institución contaba con 23 maestros de aula, 2 subdirectores, 2 secretarias y un maestro de Educación Física.<br><br>

Para el año 1992 al ser jubilado el director, ocupa la dirección la Profesora Alicia Castillo de Cedeño, en esa oportunidad la institución contaba con 4 subdirectores: Dolores de Alfonzo, Jesús Hernández,  Martina Gómez de Villarroel, María Montaño y 31 docentes de aula ( Primera y Segunda Etapa de educación Básica), 4 docentes de Educación Preescolar, 2 bibliotecarias, 6 docentes especialistas encargados de atender las áreas: Educación Física, Música,  Lectura Escritura, con una matrícula de 790 estudiantes.<br><br>

En el año 1997, a  consecuencia del terremoto  ocurrido en el Estado Sucre, la escuela sufrió daños en su infraestructura, agrietándose las paredes, pisos y techos, motivo por lo cual era imposible cumplir con las labores educativas dentro de la institución desarrollándose el proceso pedagógico en carpas y jardines, luego  por decisión de las autoridades conjuntamente con la Comunidad Educativa  hubo que abandonar las instalaciones, trasladándose entonces  las labores educativas a la sede de la Guarnición Militar, donde se permaneció durante el periodo escolar  1997- 1998.<br><br>

Para el año escolar 1998 - 1999  con la colaboración de los padres y representantes y los docentes de la institución se alquila una casa en la calle Independencia Nº  99 donde laboró por 2 años escolares consecutivos.<br><br>

A partir del 16 de septiembre del año 2000, la institución pasa a ser Unidad Educativa Bolivariana “Pedro Elías Aristeguieta”  con una nueva infraestructura, las actividades pedagógicas de Educación Básica vuelven a su sede original  permaneciendo aún la Educación Inicial en la sede alquilada de calle Independencia  y la institución cuenta para entonces con un personal conformado por: 1 Coordinadora Administrativa Alicia Castillo de Cedeño, 2 coordinadoras Pedagógica  Aida Gómez de Fernández y Elvira Codallo de González, 4 secretarias, 7 maestros Especialistas, 4 docentes de Educación Preescolar con sus respectivos Auxiliares y 20 docentes de Aula de Primera y Segunda Etapa de Educación Básica, 12 Obreros y un personal que atiende el Programa de  Alimentación  Escolar.<br><br>

A  partir del  16 de septiembre de 2006, Jubilada la Directora anterior asume la responsabilidad de dirigir como Directora (E) la profesora Elvira Codallo de González, se termina la construcción de los módulos de educación inicial y los estudiantes son incorporados a sus ambientes de aprendizajes y se inician la construcción de  nuevos espacios  para la consolidación de la institución.<br><br>

A partir del 07 de Octubre de 2.008 la  institución es dirigida por  la Licenciada Noelia Rojas de Rodríguez, acompañada por  la subdirectora la Profesora Luisa Villarroel de Sánchez, 36 docentes, 4 atienden Educación Inicial con sus respectivos Auxiliares, 21 docentes de aula, 7 Especialistas y 4 docentes en funciones administrativas con una matrícula de 646 estudiantes, 19  obreros  (2 mayordomos, 1 mensajero y 16 aseadores).<br><br>

A partir del año 2.010 la institución es dirigida por el Prof. Osmel Gómez Narváez acompañado por las Subdirectoras Profesoras Lennis Rondón y el Midelis Aliendres; con una matrícula de 616 estudiantes.<br><br>

En los actuales momentos la escuela está dirigida por la Profa. Midelis Aliendres acompañada por la Subdirectora Pedagógica Yuslemis Ramírez y Académica Lennis Rondón; con una matrícula de  631 estudiantes.  

